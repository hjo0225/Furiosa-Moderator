"""playbook — 정성조사 기법 사전 (결정론). `_reference` 의 '5 Why' 승격판.

T0 게이트 판정에 따라 LLM 이 고르지 않는다 — 행동(action)이 기법을 결정한다.
"""
from __future__ import annotations

_TECHNIQUES = {
    ("probe", "심화"): (
        "[기법: 5 Why / 래더링] 답의 '이유' 밑으로 내려가세요. 속성→혜택→가치 순으로 "
        "'그게 왜 중요하세요?'를 자연스럽게 변주합니다. 같은 문형 반복은 금지."
    ),
    ("probe", "구체화"): (
        "[기법: CIT(결정적 사건)] 일반론 대신 최근의 구체적 한 장면을 요청하세요 — "
        "'가장 최근에 그랬던 때가 언제였어요? 그때 상황이 어땠나요?'"
    ),
    ("challenge", ""): (
        "[기법: 모순 확인 화법] 비난 금지, 호기심으로. 두 발언을 나란히 들려주고 "
        "'제가 맞게 이해했는지' 프레임으로 확인하세요."
    ),
    ("clarify", ""): (
        "[기법: 되묻기] 응답자의 단어를 그대로 되돌려주며 뜻을 물으세요 — "
        "'방금 ~라고 하셨는데, 그게 어떤 의미세요?'"
    ),
    ("redirect", ""): (
        "[기법: 복귀 화법] 이탈 발언을 한 문장으로 존중하고, 다리를 놓아 원 주제로 — "
        "'그 얘기도 재미있네요. 아까 하시던 ~ 얘기로 돌아가면…'"
    ),
}


def playbook(action: str, probe_type: str = "") -> str:
    return _TECHNIQUES.get((action, probe_type if action == "probe" else ""), "")
