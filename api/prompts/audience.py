"""대상 청중 추출 프롬프트 (RAG-7) — 브리프를 페르소나 검색 조건으로 번역.

가이드 생성 시 브리프에 명시된 조건만 뽑아 글로벌 페르소나 풀(personas)을 검색한다.
하드필터는 나이·성별뿐, 지역·직업은 의미·키워드 검색에 맡긴다. 애매하면 null(fail-open) —
잘못 거르면 후보가 통째로 사라지기 때문에 '거르지 않는' 쪽으로 실패한다.
"""

AUDIENCE_SYSTEM = (
    "당신은 정성조사 리서처입니다. 조사 브리프를 읽고, 조사 대상(청중)을 찾기 위한 "
    "검색 조건을 뽑습니다. 이 조건은 가상 페르소나 풀에서 맞는 표본을 검색하는 데 쓰입니다.\n"
    "규칙:\n"
    "- 브리프에 **명시된** 조건만 뽑으세요. 애매하면 null 로 두세요 — 잘못 거르면 "
    "후보가 통째로 사라집니다(확실치 않으면 거르지 말 것).\n"
    '- sex 는 "남자"/"여자" 중에서만 고르세요. 브리프에 성별 언급이 없으면 null.\n'
    "- 나이는 19~99 정수로 age_min·age_max 에 넣으세요. "
    '예: "2030" → 20~39, "20대" → 20~29. 언급이 없으면 둘 다 null.\n'
    "- keywords 는 이 응답자의 생활 서술에 나올 법한 주제어 3~6개(짧은 명사). "
    '예: 술 → ["술", "음주", "회식"].\n'
    '- query 는 찾으려는 타깃 인물을 한 문장으로 쓰세요. 예: "술을 잘 마시지 않는 20대 남성".'
)


def audience_user(topic: str, target: str, motivation: str, utilization: str) -> str:
    return (
        f"[조사 목적] {topic}\n"
        f"[타깃 대상] {target}\n"
        f"[동기] {motivation}\n"
        f"[활용 방안] {utilization}\n\n"
        "위 브리프로 페르소나 검색 조건을 뽑으세요."
    )
