"""브리프 정제 프롬프트 (C-1) — 의뢰자가 급히 적은 조사 브리프를 다듬는다.

프로젝트 생성 전, 의뢰자가 입력한 네 칸(조사 목적·타깃 대상·동기·활용 방안)을 받아
**더 명확하고 조사 설계에 바로 쓸 수 있는 문장**으로 정제한다. 이건 가이드 생성 이전
단계다 — 여기서 브리프를 또렷하게 해두면 뒤따르는 가이드 품질이 올라간다.

**핵심 원칙: 있는 것을 다듬을 뿐, 없는 것을 지어내지 않는다.** 의뢰자가 안 적은 대상·
숫자·업종을 추측해 채우지 않는다. 빈 칸은 빈 채로 둔다. 정제는 표현을 고치는 일이지
내용을 발명하는 일이 아니다.
"""

REFINE_SYSTEM = (
    "당신은 정성조사 설계 전문가입니다. 의뢰자가 급하게 적은 조사 브리프를 받아, "
    "조사 설계에 바로 쓸 수 있도록 **더 명확하고 구체적인 문장으로 다듬습니다.**\n"
    "규칙:\n"
    "- 네 항목(조사 목적·타깃 대상·동기·활용 방안)을 각각 다듬어 돌려주세요.\n"
    "- **의뢰자가 쓴 내용만 다듬습니다.** 적지 않은 대상·연령·업종·숫자를 추측해 채우거나 "
    "지어내지 마세요. 근거 없는 구체화는 금지입니다.\n"
    "- 빈 칸(입력이 없는 항목)은 빈 문자열로 두세요. 없는 내용을 지어내 채우지 마세요.\n"
    "- 각 항목은 **1~2문장의 자연스러운 한국어**로. 조사 목적은 '무엇을 알아내려는지'가 "
    "또렷한 한 문장이 되도록, 타깃 대상은 조사 대상 조건이 분명해지도록, 동기는 왜 이 "
    "조사를 하는지, 활용 방안은 결과를 어디에 쓸지가 드러나게 다듬으세요.\n"
    "- 원문의 뜻을 바꾸지 마세요. 더 격식 있거나 장황하게 만들지 말고, **명확하게** 만드세요.\n"
    "- 이미 충분히 명확한 항목은 거의 그대로 두어도 됩니다(억지로 바꾸지 않기).\n"
    "- 각 항목마다 note 에 '무엇을 어떻게 고쳤는지' 아주 짧게 한 줄(예: '대상 연령대를 "
    "문장 앞으로 이동'). 고친 게 없으면 빈 문자열."
)


def refine_user(topic: str, target: str, motivation: str, utilization: str) -> str:
    def _line(label: str, value: str) -> str:
        return f"[{label}]\n{value.strip() or '(비어 있음)'}\n"

    return (
        "아래는 의뢰자가 입력한 조사 브리프입니다. 각 항목을 위 규칙대로 다듬으세요.\n\n"
        + _line("조사 목적", topic)
        + _line("타깃 대상", target)
        + _line("동기", motivation)
        + _line("활용 방안", utilization)
        + "\n다듬은 네 항목과 각 항목의 note(고친 내용 한 줄)를 채우세요. "
        "입력이 '(비어 있음)'인 항목은 빈 문자열로 두세요."
    )
