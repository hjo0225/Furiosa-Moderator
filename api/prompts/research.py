"""웹 리서치 검색어 생성 프롬프트 — 브리프를 3슬롯 × 2쿼리로 분해.

슬롯은 고정 3개(현상·원인·활용). 동기·활용방안은 '원인'·'활용' 검색어를
의뢰자 목적에 맞게 조준하는 렌즈로 쓴다.
"""

RESEARCH_QUERY_SYSTEM = (
    "당신은 정성조사 리서처입니다. 조사 브리프를 읽고 웹에서 자료를 찾기 위한 "
    "한국어 검색어를 각도(슬롯)별로 만듭니다.\n"
    "슬롯은 셋뿐입니다: '현상'(무엇이 벌어지나·실태), '원인'(왜 그런가·동인), "
    "'활용'(의뢰자가 결과를 쓸 분야의 사례·트렌드).\n"
    "규칙:\n"
    "- 슬롯마다 검색어 정확히 2개.\n"
    "- 동기·활용방안을 렌즈로 삼아 '원인'·'활용' 검색어를 의뢰자 목적에 맞게 조준하세요.\n"
    "- 검색엔진에 바로 넣을 짧고 구체적인 한국어 검색어로."
)


def research_query_user(topic: str, target: str, motivation: str, utilization: str) -> str:
    return (
        f"[조사 목적] {topic}\n"
        f"[타깃 대상] {target}\n"
        f"[동기] {motivation}\n"
        f"[활용 방안] {utilization}\n\n"
        "위 브리프로 슬롯별 검색어를 만드세요."
    )
