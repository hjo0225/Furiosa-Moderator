"""PII 마스킹 (M-5) — 저장 전에 개인식별정보를 지운다.

PRD 9절 / 아키텍처 §9: "원문 저장 전 개인식별정보를 마스킹, 응답자는 익명 ID로만 관리".

설계 판단 — 정규식 우선, LLM 은 쓰지 않는다:
  마스킹은 '저장 경로'에 있다. LLM 을 태우면 턴마다 지연이 붙고, 더 나쁜 건
  LLM 이 실패·타임아웃했을 때 원문이 그대로 저장될 위험이 생긴다는 점이다.
  결정론적이고 항상 성공하는 정규식이 이 자리에 맞다.

한계(의도적): 문맥 없는 한국어 인명은 잡지 않는다. '김철수'와 '김밥'을 정규식으로
구분할 수 없어서다. 대신 조사·호칭이 붙은 형태(님/씨/양/군)만 보수적으로 잡는다.
이름 전면 탐지가 필요해지면 별도 NER 을 붙이는 게 맞지 저장 경로에 LLM 을 넣는 게 아니다.
"""
from __future__ import annotations

import re

# (패턴, 치환토큰) — 순서가 중요하다. 주민번호를 전화번호보다, 전화번호를 계좌번호보다 먼저 잡는다.
#
# `\b` 를 쓰지 않는다. 파이썬 정규식에서 숫자와 한글은 둘 다 워드문자라
# "010-1234-5678이고" 의 8과 이 사이에 워드경계가 없다 → `\b` 로 끝나는 패턴이 통째로 빗나간다.
# 응답자 발화는 대부분 조사가 붙은 이 형태라, 숫자 기준 룩어라운드로 처리한다.
_RULES: list[tuple[re.Pattern[str], str]] = [
    # 주민등록번호 000000-0000000
    (re.compile(r"(?<![0-9])\d{6}\s*-\s*[1-4]\d{6}(?![0-9])"), "[주민번호]"),
    # 카드번호 (4-4-4-4)
    (re.compile(r"(?<![0-9])(?:\d{4}[-\s]?){3}\d{4}(?![0-9])"), "[카드번호]"),
    # 이메일
    (re.compile(r"(?<![\w.+-])[\w.+-]+@[\w-]+\.[\w.-]+"), "[이메일]"),
    # 휴대폰 010-0000-0000 / 01000000000
    (re.compile(r"(?<![0-9])01[016-9][-\s.]?\d{3,4}[-\s.]?\d{4}(?![0-9])"), "[전화번호]"),
    # 지역번호 유선 02-000-0000 등
    (re.compile(r"(?<![0-9])0(?:2|[3-6][1-5])[-\s.]?\d{3,4}[-\s.]?\d{4}(?![0-9])"), "[전화번호]"),
    # 계좌번호로 보이는 하이픈 구분 숫자열
    (re.compile(r"(?<![0-9])\d{2,6}-\d{2,6}-\d{2,7}(?![0-9])"), "[계좌번호]"),
    # 도로명/지번 주소 — 시/도 + 시군구 + 이하
    (
        re.compile(
            r"(서울|부산|대구|인천|광주|대전|울산|세종|경기|강원|충북|충남|전북|전남|경북|경남|제주)"
            r"[가-힣0-9\s]{0,10}?(시|군|구)\s?[가-힣0-9\-\s]{1,20}?(로|길|동|읍|면)\s?[0-9\-]{0,10}"
        ),
        "[주소]",
    ),
    # 호칭이 붙은 인명 — 성+이름(2~3자) 뒤에 님/씨/양/군
    (re.compile(r"[가-힣]{2,4}(?=\s?(?:님|씨|양|군))"), "[이름]"),
]


def _apply(text: str) -> tuple[str, list[str]]:
    """규칙을 순서대로 적용하고 (마스킹된 텍스트, 걸린 종류) 를 함께 돌려준다.

    순차 적용이 핵심이다. 각 규칙을 원문에 독립적으로 물으면 '010-1234-5678' 이
    전화번호와 계좌번호 양쪽에 걸려 실제 마스킹 결과와 어긋난 보고가 나간다.
    """
    out = text
    found: list[str] = []
    for pattern, token in _RULES:
        out, n = pattern.subn(token, out)
        if n:
            label = token.strip("[]")
            if label not in found:
                found.append(label)
    return out, found


def mask_pii(text: str) -> str:
    """텍스트에서 개인식별정보를 토큰으로 치환해 돌려준다."""
    if not text:
        return text
    return _apply(text)[0]


def scan_pii(text: str) -> list[str]:
    """어떤 종류의 PII 가 검출됐는지 목록만 반환(로깅·감사용, 원문은 담지 않는다)."""
    if not text:
        return []
    return _apply(text)[1]
